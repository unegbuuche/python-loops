# for loop for even numbers between 1 - 30
for i in range(2,31, 2) :
    print(i)

# while loop for even numbers between 1 - 30
num = 2
while num <= 30 :
    print(num)
    num += 2

# for loop for odd numbers between 1 - 30
for i in range(1,30, 2) :
    print(i)

# while loop for odd numbers between 1 - 30
num = 1
while num <= 30 :
    print(num)
    num += 2
